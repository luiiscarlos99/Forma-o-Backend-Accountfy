package com.accountfy.livros.entities;

public class AnoMes {
	private String Ano;
	private String Mes;
	
	public AnoMes() {
		
	}

	public AnoMes(String ano, String mes) {
		Ano = ano;
		Mes = mes;
	}

	public String getAno() {
		return Ano;
	}

	public void setAno(String ano) {
		Ano = ano;
	}

	public String getMes() {
		return Mes;
	}

	public void setMes(String mes) {
		Mes = mes;
	}
	
	
}
